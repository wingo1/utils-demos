package com.wingo1.example.decimal;

public class DecimalRepresentation {

	public static void main(String[] args) {
		int a = 11;
		int b = 011;// 八进制
		int c = 0x11;// 十六进制
		int d = 0b11;// 二进制
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}

}
